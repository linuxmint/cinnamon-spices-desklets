const NEPALI_NUMERALS = ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'];

function toDevanagari(num) {
    return num.toString().split('').map(c => {
        const n = parseInt(c);
        return isNaN(n) ? c : NEPALI_NUMERALS[n];
    }).join('');
}

function toBikramSambat(date) {
    const targetUtcDate = new Date(Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()));
    const startDate = new Date(Date.UTC(Bsdata.BS_START_DATE_AD[0], Bsdata.BS_START_DATE_AD[1], Bsdata.BS_START_DATE_AD[2]));
    
    let remainingDays = Math.round((targetUtcDate.getTime() - startDate.getTime()) / 86400000);
    if (remainingDays < 0) return null;

    for (let y = 0; y < Bsdata.NP_MONTHS_DATA.length; y++) {
        const yearData = Bsdata.NP_MONTHS_DATA[y];
        const daysInYear = yearData.reduce((sum, current) => sum + current, 0);
        if (remainingDays < daysInYear) {
            for (let m = 0; m < 12; m++) {
                if (remainingDays < yearData[m]) {
                    return { year: Bsdata.BS_START_YEAR + y, monthIndex: m, day: remainingDays + 1, monthName: NEPALI_BS_MONTHS[m] };
                }
                remainingDays -= yearData[m];
            }
        }
        remainingDays -= daysInYear;
    }
    return null;
}

function fromBikramSambat(bsYear, monthIndex, day) {
    const yearIndex = bsYear - Bsdata.BS_START_YEAR;
    if (yearIndex < 0 || yearIndex >= Bsdata.NP_MONTHS_DATA.length) return null;
    
    let daysOffset = day - 1;
    for (let y = 0; y < yearIndex; y++) {
        daysOffset += Bsdata.NP_MONTHS_DATA[y].reduce((s, c) => s + c, 0);
    }
    for (let m = 0; m < monthIndex; m++) {
        daysOffset += Bsdata.NP_MONTHS_DATA[yearIndex][m];
    }
    
    const resultDate = new Date(Date.UTC(Bsdata.BS_START_DATE_AD[0], Bsdata.BS_START_DATE_AD[1], Bsdata.BS_START_DATE_AD[2]));
    resultDate.setUTCDate(resultDate.getUTCDate() + daysOffset);
    return resultDate;
}

